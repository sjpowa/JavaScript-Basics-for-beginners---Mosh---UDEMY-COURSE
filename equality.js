// Strict Equality
// this means that this works with
// same Type + Value
// so === can do the equality beetween the same type
console.log(1 === 1)
console.log('1' === 1)
console.log('ciao' === 'ciao');


// Lose Equality, double ==
// this instead does not care the type
// and it will convert automatically a string to
// a number like as u can see in the example down here
console.log(1 == 1)
console.log('1' == 1)
console.log(true == 1)
console.log(false == 0)

// remember that the boolean values are:
// true is 1
// false is 0
