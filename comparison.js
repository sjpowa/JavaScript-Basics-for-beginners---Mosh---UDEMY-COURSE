let x = 2;

console.log(x > 0); // x e' maggiore di 0? True
// so we have in the console a bool answer

console.log(x >= 2); // true
console.log(x > 2); // false
console.log(x < 2); // false
console.log(x <= 2); // true

// EQUALITY OPERATOR is done with 3 =, ===
console.log(x === 2); // true
// NOT EQUAL needs a ! + 2 =, !==
console.log(x !== 2); // false

